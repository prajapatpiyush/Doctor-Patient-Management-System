    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        
        String name = txtName.getText().trim();
String mobile = txtMobile.getText().trim();
if (!mobile.matches("\\d{10}")) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Mobile number must be exactly 10 digits");
    return;
}
String dateStr = txtDate.getText().trim();
String treatment = txtTreatment.getText().trim();
String notes = txtNotes.getText().trim();

// Validation
if (name.isEmpty() || mobile.isEmpty() || dateStr.isEmpty() || treatment.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Please fill all required fields");
    return;
}

// Date conversion
java.util.Date visitDate;
try {
    visitDate = java.sql.Date.valueOf(dateStr);
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Invalid date format. Use YYYY-MM-DD");
    return;
}

// Create Patient object
Patient patient = new Patient(
        name,
        mobile,
        visitDate,
        treatment,
        notes
);

// Save using controller
boolean saved = patientController.savePatient(patient);

if (saved) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Patient record saved successfully");

    // Clear fields
    txtName.setText("");
    txtMobile.setText("");
    txtDate.setText("");
    txtTreatment.setText("");
    txtNotes.setText("");
} else {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Error saving patient record");
}
if (saved) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Patient record saved successfully");

    txtName.setText("");
    txtMobile.setText("");
    txtDate.setText("");
    txtTreatment.setText("");
    txtNotes.setText("");

    loadPatientsTable(); // REFRESH TABLE
}
    }//GEN-LAST:event_btnSaveActionPerformed

