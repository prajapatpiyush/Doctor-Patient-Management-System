    /**
     * @param args the command line arguments
     */
    public void loadPatientsTable() {
      
    javax.swing.table.DefaultTableModel model =
            new javax.swing.table.DefaultTableModel(
                new Object[]{"S.No", "Name", "Mobile", "Date", "Treatment", "Notes"},
                0
            );

    tblPatients.setModel(model);

    java.util.List<model.Patient> list =
            patientController.getAllPatients();

    java.text.SimpleDateFormat sdf =
            new java.text.SimpleDateFormat("dd-MM-yyyy");

    for (model.Patient p : list) {

        String formattedDate = "";
        if (p.getVisitDate() != null) {
            formattedDate = sdf.format(p.getVisitDate());
        }

        Object[] row = {
    p.getPatientId(),
    p.getName(),
    p.getMobile(),
    formattedDate,
    p.getTreatment(),
    p.getNotes()   // âœ… ADD THIS
};

        model.addRow(row);
    }
}


    
