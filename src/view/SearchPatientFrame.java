    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
       
        String searchValue = txtSearchValue.getText().trim();
    if (searchValue.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this,
            "Please enter search value");
        return;
    }

    String searchType = cmbSearchType.getSelectedItem().toString();
    Patient patient = null;

    PatientController controller = new PatientController();

    if (searchType.equals("Patient ID")) {

        int patientId;
        try {
            patientId = Integer.parseInt(searchValue);
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this,
                "Patient ID must be a number");
            return;
        }

        patient = controller.getPatientById(patientId);

    } else if (searchType.equals("Name")) {

        patient = controller.getPatientByName(searchValue);
    }
    else if (searchType.equals("Mobile")) {

    patient = controller.getPatientByMobile(searchValue);
}


   if (patient == null) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Patient not found");
    setFormEnabled(false);   // ðŸ”’ lock form
    currentPatientId = -1;
    return;
}
currentPatientId = patient.getPatientId();
    txtName.setText(patient.getName());
    txtMobile.setText(patient.getMobile());
    txtDate.setText(patient.getVisitDate().toString());
    txtTreatment.setText(patient.getTreatment());
    txtNotes.setText(patient.getNotes());
    setFormEnabled(true); // âœ… allow edit now
    
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (currentPatientId == -1) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Please search a patient first");
    return;
}

String name = txtName.getText().trim();
String mobile = txtMobile.getText().trim();
if (!mobile.matches("\\d{10}")) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Mobile number must be exactly 10 digits");
    return;
}
String dateStr = txtDate.getText().trim();
String treatment = txtTreatment.getText().trim();
String notes = txtNotes.getText().trim();


if (name.isEmpty() || mobile.isEmpty() || dateStr.isEmpty() || treatment.isEmpty()) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "All required fields must be filled");
    return;
}

java.util.Date visitDate;
try {
    visitDate = java.sql.Date.valueOf(dateStr);
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Invalid date format (YYYY-MM-DD)");
    return;
}

Patient patient = new Patient(
    currentPatientId,
    name,
    mobile,
    visitDate,
    treatment,
    notes
);

boolean updated = new PatientController().updatePatient(patient);

if (updated) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Patient record updated successfully");
}

else {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Update failed");
}
mainFrame.loadPatientsTable(); // ðŸ”„ refresh dashboard
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (currentPatientId == -1) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Please search a patient first");
    return;
}

// Confirmation dialog
int confirm = javax.swing.JOptionPane.showConfirmDialog(
        this,
        "Are you sure you want to delete this patient?",
        "Confirm Delete",
        javax.swing.JOptionPane.YES_NO_OPTION
);

if (confirm != javax.swing.JOptionPane.YES_OPTION) {
    return;
}

boolean deleted = new PatientController().deletePatient(currentPatientId);

if (deleted) {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Patient deleted successfully");

    // Clear fields
    txtName.setText("");
    txtMobile.setText("");
    txtDate.setText("");
    txtTreatment.setText("");
    txtNotes.setText("");

    currentPatientId = -1; // reset
    setFormEnabled(false);
} else {
    javax.swing.JOptionPane.showMessageDialog(this,
        "Delete failed");
}
mainFrame.loadPatientsTable(); // ðŸ”„ refresh dashboard
    }//GEN-LAST:event_btnDeleteActionPerformed

